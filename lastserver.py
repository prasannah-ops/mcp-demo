# Your unmodified MCP server (main.py)
MCP_UPSTREAM = "http://127.0.0.1:8000/mcp"  # â† main.py port

app = FastAPI()

# ğŸ”‘ SINGLE GLOBAL MCP SESSION (REQUIRED FOR AGENT BUILDER)
SESSION_ID = None


# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# MCP BRIDGE (AGENT BUILDER SAFE)
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@app.api_route("/mcp", methods=["POST"])
async def mcp_bridge(request: Request):
    global SESSION_ID

    # ğŸš« DO NOT PARSE
    # ğŸš« DO NOT MODIFY
    # ğŸš« DO NOT INSPECT
    body = await request.body()

    headers = {
        "Content-Type": "application/json",
        "Accept": "application/json, text/event-stream",
    }

    # âœ… Force ALL requests into the same MCP session
    if SESSION_ID:
        headers["mcp-session-id"] = SESSION_ID

    async with httpx.AsyncClient(timeout=None) as client:
        upstream = await client.post(
            MCP_UPSTREAM,
            content=body,   # RAW BYTES ONLY
            headers=headers,
        )

    # âœ… Capture MCP session ONCE (initialize)
    if "mcp-session-id" in upstream.headers and SESSION_ID is None:
        SESSION_ID = upstream.headers["mcp-session-id"]

    return Response(
        content=upstream.content,
        status_code=upstream.status_code,
        headers={
            "Content-Type": upstream.headers.get(
                "content-type", "application/json"
            ),
            **({"mcp-session-id": SESSION_ID} if SESSION_ID else {}),
        },
    )


